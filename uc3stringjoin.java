public class uc3stringjoin {

    public static void main(String[] args) {

        System.out.println(String.join(" ", " ***** ", " ***** ", " ***** ", " ***** "));
        System.out.println(String.join(" ", "*     *", "*     *", "*     *", "*      "));
        System.out.println(String.join(" ", "*     *", "*     *", "*     *", "*      "));
        System.out.println(String.join(" ", "*     *", "*     *", " ***** ", " ***** "));
        System.out.println(String.join(" ", "*     *", "*     *", "*      ", "      *"));
        System.out.println(String.join(" ", "*     *", "*     *", "*      ", "      *"));
        System.out.println(String.join(" ", " ***** ", " ***** ", "*      ", " ***** "));
    }
}

     


    
    

